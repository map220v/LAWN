namespace Lawn
{
	internal class SpecialGridPlacement
	{
		public int mPixelX;

		public int mPixelY;

		public int mGridX;

		public int mGridY;

		public SpecialGridPlacement(int aPixelX, int aPixelY, int aGridX, int aGridY)
		{
			mPixelX = aPixelX;
			mPixelY = aPixelY;
			mGridX = aGridX;
			mGridY = aGridY;
		}
	}
}
